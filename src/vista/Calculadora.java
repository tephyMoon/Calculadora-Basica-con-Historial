     // Método para reproducir sonido
    private void playSound(String soundFile) {
        try {
            File file = new File(soundFile);
            AudioInputStream audioStream = AudioSystem.getAudioInputStream(file);
            Clip clip = AudioSystem.getClip();
            clip.open(audioStream);
            clip.start();
        } catch (UnsupportedAudioFileException | IOException | LineUnavailableException ex) {
            ex.printStackTrace();
        }
    }
    
// Método para crear el archivo si no existe
private void crearArchivo() {
    File archivo = new File("BitacoraCalculadora.txt");
    try {
        if (archivo.createNewFile()) {
            System.out.println("Archivo creado: " + archivo.getName());
        } else {
            System.out.println("El archivo ya existe.");
        }
    } catch (IOException e) {
        System.out.println("Ocurrió un error al crear el archivo.");
        e.printStackTrace();
    }
}
    
// Método para escribir en el archivo
private void escribirEnArchivo(String texto) {
    try (FileWriter fw = new FileWriter("BitacoraCalculadora.txt", true);
         PrintWriter pw = new PrintWriter(fw)) {
        pw.println(texto);
    } catch (IOException e) {
        e.printStackTrace();
    }
}
// Método para abrir el archivo de texto
private void abrirHistorial() {
    // Ruta del archivo de texto
    File archivo = new File("BitacoraCalculadora.txt");
    
    // Verifica si el archivo existe
    if (archivo.exists()) {
        try {
            Desktop.getDesktop().open(archivo);
        } catch (IOException e) {
            System.out.println("Ocurrió un error al intentar abrir el archivo.");
            e.printStackTrace();
        }
    } else {
        System.out.println("El archivo no existe.");
    }
}
