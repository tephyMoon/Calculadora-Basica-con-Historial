    private void btn_igualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_igualActionPerformed
       this.segundonumero=Float.parseFloat(this.lbl_casilla.getText());
       
       switch(this.operador){
            case "+" -> {this.lbl_casilla.setText(this.sincero(this.primernumero+this.segundonumero));
                            escribirEnArchivo(this.primernumero + " " + this.operador + " " + this.segundonumero + " = " + this.lbl_casilla.getText());}
            case "-" -> {this.lbl_casilla.setText(this.sincero(this.primernumero-this.segundonumero));
                            escribirEnArchivo(this.primernumero + " " + this.operador + " " + this.segundonumero + " = " + this.lbl_casilla.getText());}
            case "*" -> {this.lbl_casilla.setText(this.sincero(this.primernumero*this.segundonumero));
                            escribirEnArchivo(this.primernumero + " " + this.operador + " " + this.segundonumero + " = " + this.lbl_casilla.getText());}
            case "/" -> {
                        if (this.segundonumero==0) {
                        this.lbl_casilla.setText("ERROR");
                        escribirEnArchivo(this.primernumero + " " + this.operador + " " + this.segundonumero + " = " + this.lbl_casilla.getText());
                        } else {
                        this.lbl_casilla.setText(this.sincero(this.primernumero/this.segundonumero));
                        escribirEnArchivo(this.primernumero + " " + this.operador + " " + this.segundonumero + " = " + this.lbl_casilla.getText());
                        }
                    }
       }
       playSound("short-beep-tone-47916.wav");
    }//GEN-LAST:event_btn_igualActionPerformed

